// hover on discover
$(".discover-text").hover(
  function () {
    $(".discover-text-2").css("position", "relative");
    $(".discover-text-2").css("left", "0.5em");
  },
  function () {
    $(".discover-text-2").css("position", "relative");
    $(".discover-text-2").css("left", "0em");
  }
);

